let supported_operators () =
  print_endline
    {|
  '+' - addition 
  '-' - subtraction 
  '*' - multiplication 
  '/' - division
  '^' - power
  '()' - brackets
  |}

let rec loop () =
  print_string "> ";
  let maths = read_line () in
  match maths with
  | "q" -> exit 0
  | "h" ->
      supported_operators ();
      loop ()
  | _ ->
      let tokens = Lexer.lex maths in
      let ast, _ = Parser.parse_expr tokens in

      let answer = Eval.eval ast in

      answer |> string_of_float |> print_endline;
      loop ()

